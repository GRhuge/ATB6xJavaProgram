package ex_18042024_Operators;

public class Lab048 {

	public static void main(String[] args) {
		int a = 10;
		System.out.println(++a + ++a);
		
		// Part A --> ++a , Ep_Result = 11 , a = 11
		// Part B --> + , Ep_Result, a
		// Part C --> ++a , EP_Result = 12 , a = 12 --> In Part A it was 11 so it becomes 12 in Part C

	}

}
