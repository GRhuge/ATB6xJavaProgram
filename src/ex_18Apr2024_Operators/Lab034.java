package ex_18Apr2024_Operators;

public class Lab034 {

	public static void main(String[] args) {
		// Logical Operators --> Not Gate, OR Gate, And Gate
		
		boolean a = true;
		System.out.println(!a); // <--- Not Gate example
		System.out.println(!(10>20)); // <--- Not Gate example
		System.out.println(!(30>90));
		System.out.println(!!(30>90));
	}

}
