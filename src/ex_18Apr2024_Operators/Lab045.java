package ex_18Apr2024_Operators;

public class Lab045 {

	public static void main(String[] args) {
		// Increment (++) / Decrement (--) Operators
		
		// pre, post
		
		// Post --> The value of the operand is used in the expression first, and then it is incremented or decremented
		
		int a = 10;
		int b = a++; // value assign first then incremented 
		System.out.println(b);
		System.out.println(a);
	}

}
